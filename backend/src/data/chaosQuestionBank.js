const formatClueForRoast = (clue = "") =>
  clue
    .replace(/\.{3}/g, " ")
    .replace(/\s+/g, " ")
    .trim();

const buildExtraOperatorNote = (question, index) => {
  const openers = EXTRA_OPERATOR_OPENERS[question.signal] || EXTRA_OPERATOR_OPENERS.STATIC;
  const opener = openers[index % openers.length];
  const clueLine = formatClueForRoast(question.clue);
  return `${opener} ${clueLine} Read the roast vibe and send your signal.`;
};

